function renderTwoColumns(doc: jsPDF, result: SearchResult, m: number, y: number, cw: number): number {
  const leftW = cw * 0.55, rightW = cw * 0.42, rightX = m + leftW + cw * 0.03;
  const startY = y, stats = result.summaryStats;

  // LEFT: History Summary bullets
  const bullets: string[] = [];
  const sorted = [...result.events].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
  if (sorted.length > 0) bullets.push(`${formatEventDate(sorted[0].date)} represents the most recent hail activity within close proximity of the property.`);
  if (stats.clusteredStormDates.length > 0) bullets.push("Multiple hail events were recorded on the same date, indicating a sustained storm system.");
  if (stats.largestHail !== null && stats.largestHail >= 1.75) bullets.push(`Hail measuring up to ${stats.largestHail} inches has been recorded in the surrounding area.`);
  if (stats.highestWind !== null && stats.highestWind >= 55) bullets.push(`Wind activity up to ${stats.highestWind} mph has also been documented within proximity of the property.`);
  const uniqueYears = new Set(result.events.map(e => new Date(e.date).getFullYear()));
  if (uniqueYears.size >= 3) bullets.push("Repeated storm exposure has occurred over multiple years.");

  const leftBoxH = Math.max(bullets.length * 8 + 10, 68); // Taller to give DATA METHODOLOGY box room
  doc.setFillColor(...C.lightGray); doc.roundedRect(m, y, leftW, leftBoxH, 3, 3, "F");
  doc.setFont("helvetica", "bold"); doc.setFontSize(8); doc.setTextColor(...C.dark);
  doc.text("HISTORY SUMMARY", m + PAD, y + 7);
  let ly = y + 14;
  bullets.forEach(b => {
    doc.setFont("helvetica", "bold"); doc.setFontSize(8); doc.setTextColor(...C.gold);
    doc.text("v", m + PAD, ly);
    doc.setFont("helvetica", "normal"); doc.setFontSize(7.5); doc.setTextColor(...C.dark);
    const w = doc.splitTextToSize(b, leftW - PAD * 2 - 6);
    doc.text(w, m + PAD + 5, ly);
    ly += w.length * 3.5 + 2;
  });

  // RIGHT: Gauge (15% smaller)
  const isHigh = stats.urgentEvents > 0 || (stats.largestHail !== null && stats.largestHail >= 1.75);
  const isMod = stats.recentEvents > 0 || stats.totalEvents >= 5;
  const riskLevel = isHigh ? "High" : isMod ? "Elevated" : "Low";
  const riskColor = isHigh ? C.red : isMod ? C.amber : C.green;

  const gaugeBoxH = 44;
  doc.setFillColor(230, 230, 235); doc.roundedRect(rightX + 0.6, startY + 0.6, rightW, gaugeBoxH, 3, 3, "F");
  doc.setFillColor(...C.lightGray); doc.roundedRect(rightX, startY, rightW, gaugeBoxH, 3, 3, "F");

  const gx = rightX + rightW / 2, gy = startY + 16, gr = 13; // 15% smaller
  drawGauge(doc, gx, gy, gr);
  const nPct = isHigh ? 0.82 : isMod ? 0.5 : 0.18;
  const nA = Math.PI + nPct * Math.PI;
  doc.setDrawColor(...C.dark); doc.setLineWidth(1.0);
  doc.line(gx, gy, gx + Math.cos(nA) * (gr - 3), gy + Math.sin(nA) * (gr - 3));
  doc.setFillColor(...C.dark); doc.circle(gx, gy, 1.5, "F");

  doc.setFont("helvetica", "normal"); doc.setFontSize(5); doc.setTextColor(...C.grayMed);
  const rl = "RISK LEVEL"; doc.text(rl, gx - doc.getTextWidth(rl) / 2, gy + 5);
  doc.setFont("helvetica", "bold"); doc.setFontSize(13); doc.setTextColor(...riskColor);
  doc.text(riskLevel, gx - doc.getTextWidth(riskLevel) / 2, gy + 10);

  // 2x2 grid bullets below gauge
  const gridY = startY + gaugeBoxH - 12;
  const colL = rightX + PAD, colR = rightX + rightW / 2 + 2;
  const gridItems = [
    ["Hail activity", "within 2 miles"],
    ["Multiple past", "storm events"],
    ["NOAA radar-", "confirmed data"],
    ...(stats.highestWind !== null && stats.highestWind >= 55 ? [["Wind damage", "threshold exceeded"]] : [["", ""]]),
  ];
  for (let i = 0; i < Math.min(gridItems.length, 4); i++) {
    const col = i % 2 === 0 ? colL : colR;
    const row = Math.floor(i / 2);
    const gy2 = gridY + row * 5;
    if (gridItems[i][0]) {
      doc.setFont("helvetica", "bold"); doc.setFontSize(4.5); doc.setTextColor(...C.gold);
      doc.text("v", col, gy2);
      doc.setFont("helvetica", "normal"); doc.setFontSize(4.5); doc.setTextColor(...C.dark);
      doc.text(gridItems[i][0], col + 4, gy2);
      doc.text(gridItems[i][1], col + 4, gy2 + 2.5);
    }
  }

  // DATA METHODOLOGY
  const ry = startY + gaugeBoxH + 2;
  const wmH = leftBoxH - gaugeBoxH - 2;
  if (wmH > 10) {
    doc.setFillColor(...C.lightGray); doc.roundedRect(rightX, ry, rightW, wmH, 3, 3, "F");
    doc.setFont("helvetica", "bold"); doc.setFontSize(6.5); doc.setTextColor(...C.dark);
    doc.text("DATA METHODOLOGY", rightX + PAD, ry + 5);
    doc.setFont("helvetica", "normal"); doc.setFontSize(6); doc.setTextColor(...C.grayLight);
    doc.text(doc.splitTextToSize("Based on recent storm activity, this property falls within a standard insurance evaluation window. A professional roof inspection is recommended to assess potential damage before the window closes.", rightW - PAD * 2), rightX + PAD, ry + 10);
  }

  return startY + leftBoxH + PAD;
}

