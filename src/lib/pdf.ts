function dSev(l: string | null): string { return l === "Minor" ? "Impacted" : l || "--"; }

function findBestClaim(events: StormEvent[]) {
  const now = new Date();
  const sorted = [...events].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
  for (const e of sorted) {
    const d = Math.floor((now.getTime() - new Date(e.date).getTime()) / 864e5);
    if (d <= 365) return { event: e, tier: "claim" as const, daysRemaining: 365 - d };
    if (d <= 730) return { event: e, tier: "review" as const, daysRemaining: Math.max(0, 365 - d) };
  }
  return { event: null, tier: "none" as const, daysRemaining: 0 };
}

/** Clean radar narrative, filtering jargon */
function cleanRadar(raw: string): string {
  // Filter out forecaster jargon
  if (/as of \d+\s*(am|pm)/i.test(raw)) return "";
  if (/sounding/i.test(raw)) return "";
  if (/temperature and dewpoint/i.test(raw)) return "";
  if (/there were no storms yet/i.test(raw)) return "";
  if (/^a strong storm/i.test(raw)) return ""; // Generic NWS episode narrative, not property-specific
  if (/^unsettled weather/i.test(raw)) return ""; // Generic weather description
  const r = raw.match(/Radar[:\s]+(\w+)/i);
  const p = raw.match(/(\d+)%\s*prob/i);
  if (r && p) return `Radar: ${r[1]} — ${p[1]}% probability`;
  if (r) return `Radar: ${r[1]}`;
  return raw.length > 55 ? raw.substring(0, 55) + "..." : raw;
}

function sevColor(l: string | null): RGB {
  if (l === "Severe" || l === "Significant" || l === "Damaging" || l === "Destructive" || l === "Extreme") return C.red;
  if (l === "Moderate") return C.amber;
  return C.gray;
}

function buildProximityData(events: StormEvent[]) {
  const dateMap = new Map<string, StormEvent[]>();
  events.filter(e => e.type === "HAIL" && e.magnitude).forEach(e => {
    const dk = e.date.split("T")[0];
    if (!dateMap.has(dk)) dateMap.set(dk, []);
    dateMap.get(dk)!.push(e);
  });
  const rows: Array<{ date: string; location: string; m1: string; m2: string; m3: string; max: number }> = [];
  for (const [dk, evts] of dateMap) {
    const g = (d: number) => evts.filter(e => e.distanceFromProperty <= d).reduce((mx, e) => Math.max(mx, e.magnitude || 0), 0);
    const m1 = g(1), m2 = g(2), m3 = g(3);
    rows.push({ date: formatEventDate(dk + "T12:00:00Z"), location: evts[0].countyName || "Saline County",
      m1: m1 > 0 ? `${m1}"` : "--", m2: m2 > 0 ? `${m2}"` : "--", m3: m3 > 0 ? `${m3}"` : "--", max: Math.max(m1, m2, m3) });
  }
  return rows.sort((a, b) => b.max - a.max).slice(0, 8);
}

