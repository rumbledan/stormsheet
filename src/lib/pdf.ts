function drawGauge(doc: jsPDF, cx: number, cy: number, r: number) {
  [
    { s: Math.PI, e: Math.PI * 1.33, c: C.green },
    { s: Math.PI * 1.34, e: Math.PI * 1.66, c: C.amber },
    { s: Math.PI * 1.67, e: Math.PI * 2, c: C.red },
  ].forEach(({ s, e, c }) => {
    doc.setDrawColor(...c); doc.setLineWidth(4);
    for (let i = 0; i < 15; i++) {
      const a1 = s + (e - s) * (i / 15), a2 = s + (e - s) * ((i + 1) / 15);
      doc.line(cx + Math.cos(a1) * r, cy + Math.sin(a1) * r, cx + Math.cos(a2) * r, cy + Math.sin(a2) * r);
    }
  });
  doc.setLineWidth(0.3);
}

function loadImage(src: string, maxSize = 150): Promise<string> {
  return new Promise((resolve, reject) => {
    const img = new Image();
    img.crossOrigin = "anonymous";
    img.onload = () => {
      let w = img.width, h = img.height;
      if (w > maxSize || h > maxSize) { const s = maxSize / Math.max(w, h); w = Math.round(w * s); h = Math.round(h * s); }
      const canvas = document.createElement("canvas");
      canvas.width = w; canvas.height = h;
      canvas.getContext("2d")?.drawImage(img, 0, 0, w, h);
      resolve(canvas.toDataURL("image/png"));
    };
    img.onerror = reject;
    img.src = src;
  });
}
