async function renderStormDist(doc: jsPDF, result: SearchResult, mapElement: HTMLElement | null, m: number, y: number, cw: number, ph: number): Promise<number> {
  if (y > ph - 80) return y;
  doc.setFont("helvetica", "bold"); doc.setFontSize(8); doc.setTextColor(...C.dark);
  doc.text("STORM EVENT DISTRIBUTION ", m, y + 3);
  const bw = doc.getTextWidth("STORM EVENT DISTRIBUTION ");
  doc.setFont("helvetica", "normal"); doc.text("(LAST 5 YEARS)", m + bw, y + 3);
  y += 7;

  const leftW = cw * 0.48, rightW = cw * 0.49, rightX = m + leftW + cw * 0.03;
  const mapH = 38;

  // Map
  if (mapElement) {
    try {
      const canvas = await html2canvas(mapElement, { useCORS: true, scale: 2, backgroundColor: "#1a1d27" });
      doc.addImage(canvas.toDataURL("image/jpeg", 0.8), "JPEG", m, y, leftW, mapH);
      doc.setDrawColor(...C.medGray); doc.setLineWidth(0.3); doc.roundedRect(m, y, leftW, mapH, 2, 2, "S");
    } catch { doc.setFillColor(...C.lightGray); doc.roundedRect(m, y, leftW, mapH, 2, 2, "F"); }
  } else { doc.setFillColor(...C.lightGray); doc.roundedRect(m, y, leftW, mapH, 2, 2, "F"); doc.setFontSize(7); doc.setTextColor(...C.gray); doc.text("Map unavailable", m + leftW / 2 - 10, y + mapH / 2); }

  // Proximity table below map with colored headers
  let py = y + mapH + 3;
  doc.setFont("helvetica", "bold"); doc.setFontSize(5); doc.setTextColor(...C.dark);
  doc.text("HAIL STORM EVENTS — ESTIMATED SIZE AND PROXIMITY", m, py);
  py += 3;

  const proxData = buildProximityData(result.events);
  const tblW = leftW;
  const dateW = tblW * 0.25, locW = tblW * 0.30, distW = (tblW - dateW - locW) / 3;
  const pc = { date: m, loc: m + dateW, m1: m + dateW + locW, m2: m + dateW + locW + distW, m3: m + dateW + locW + distW * 2 };

  // Table border
  doc.setDrawColor(...C.medGray); doc.setLineWidth(0.3);

  // Header row
  const hh = 4;
  doc.setFillColor(...C.navyBg); doc.rect(pc.date, py, dateW, hh, "F"); doc.rect(pc.loc, py, locW, hh, "F");
  doc.setFillColor(...C.greenBg); doc.rect(pc.m1, py, distW, hh, "F");
  doc.setFillColor(...C.amberBg); doc.rect(pc.m2, py, distW, hh, "F");
  doc.setFillColor(...C.redBg); doc.rect(pc.m3, py, distW, hh, "F");

  doc.setFontSize(4.5); doc.setFont("helvetica", "bold");
  doc.setTextColor(...C.white); doc.text("Date", pc.date + 1, py + 2.8); doc.text("Location", pc.loc + 1, py + 2.8);
  doc.setTextColor(...C.greenText); doc.text("1 Mile", pc.m1 + 1, py + 2.8);
  doc.setTextColor(...C.amberText); doc.text("2 Miles", pc.m2 + 1, py + 2.8);
  doc.setTextColor(...C.redText); doc.text("3 Miles", pc.m3 + 1, py + 2.8);
  py += hh;

  proxData.forEach((row, idx) => {
    if (idx % 2 === 1) { doc.setFillColor(...C.zebraGray); doc.rect(m, py, tblW, 3.5, "F"); }
    doc.setFont("helvetica", "normal"); doc.setFontSize(4.5); doc.setTextColor(...C.dark);
    doc.text(row.date, pc.date + 1, py + 2.5);
    doc.text(row.location, pc.loc + 1, py + 2.5);
    doc.setFont("helvetica", "bold");
    doc.text(row.m1, pc.m1 + 1, py + 2.5);
    doc.text(row.m2, pc.m2 + 1, py + 2.5);
    doc.text(row.m3, pc.m3 + 1, py + 2.5);
    // Style "--" as gray italic
    if (row.m1 === "--") { doc.setFont("helvetica", "italic"); doc.setTextColor(...C.grayMed); doc.text("--", pc.m1 + 1, py + 2.5); }
    if (row.m2 === "--") { doc.setFont("helvetica", "italic"); doc.setTextColor(...C.grayMed); doc.text("--", pc.m2 + 1, py + 2.5); }
    if (row.m3 === "--") { doc.setFont("helvetica", "italic"); doc.setTextColor(...C.grayMed); doc.text("--", pc.m3 + 1, py + 2.5); }
    py += 3.5;
  });

  // Table border
  doc.setDrawColor(...C.medGray); doc.roundedRect(m, y + mapH + 6, tblW, py - (y + mapH + 6), 1, 1, "S");

  py += 1;
  doc.setFont("helvetica", "italic"); doc.setFontSize(3.5); doc.setTextColor(...C.grayMed);
  doc.text("Hail size values represent estimated maximum diameter within specified distances from the property.", m, py);
  py += 2;
  doc.text("Storm activity assessed based on proximity and meteorological relevance to the subject property.", m, py);

  // RIGHT: Abbreviated event table — tight columns
  const tc = {
    date: rightX,
    type: rightX + rightW * 0.28,
    mag: rightX + rightW * 0.42,
    dist: rightX + rightW * 0.58,
    sev: rightX + rightW * 0.72,
  };

  doc.setFont("helvetica", "bold"); doc.setFontSize(5); doc.setTextColor(...C.gray);
  doc.text("Date", tc.date, y + 3); doc.text("Type", tc.type, y + 3);
  doc.text("Mag.", tc.mag, y + 3); doc.text("Dist.", tc.dist, y + 3); doc.text("Severity", tc.sev, y + 3);
  doc.setDrawColor(...C.medGray); doc.line(rightX, y + 4.5, rightX + rightW, y + 4.5);

  let ty = y + 8;
  result.events.slice(0, 10).forEach((ev, idx) => {
    if (idx % 2 === 0) { doc.setFillColor(...C.zebraGray); doc.rect(rightX, ty - 2, rightW, 4.5, "F"); }
    doc.setFont("helvetica", "normal"); doc.setFontSize(5); doc.setTextColor(...C.dark);
    doc.text(formatEventDate(ev.date), tc.date, ty);
    doc.text(ev.type, tc.type, ty);
    doc.setFont("helvetica", "bold");
    doc.text(ev.magnitude ? `${ev.magnitude}${ev.magnitudeUnit === "inches" ? '"' : " mph"}` : "--", tc.mag, ty);
    doc.setFont("helvetica", "normal");
    doc.text(`${ev.distanceFromProperty.toFixed(1)} mi`, tc.dist, ty);
    doc.setTextColor(...sevColor(ev.severityLabel)); doc.setFont("helvetica", "bold");
    doc.text(dSev(ev.severityLabel), tc.sev, ty);
    ty += 4.5;
  });

  return Math.max(py + 3, ty + 2);
}

