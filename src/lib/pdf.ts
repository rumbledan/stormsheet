function renderStatsRow(doc: jsPDF, result: SearchResult, m: number, y: number, cw: number): number {
  const stats = result.summaryStats, bw = cw / 3, bh = 18;
  doc.setFillColor(...C.lightGray); doc.roundedRect(m, y, cw, bh, 3, 3, "F");
  const sorted = [...result.events].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
  const items = [
    { v: String(stats.hailEvents), l: "HAIL EVENTS" },
    { v: String(stats.windEvents), l: stats.windEvents === 1 ? "WIND EVENT" : "WIND EVENTS" },
    { v: sorted.length > 0 ? formatEventDate(sorted[0].date) : "N/A", l: "LAST EVENT" },
  ];
  items.forEach((it, i) => {
    const bx = m + i * bw;
    if (i > 0) { doc.setDrawColor(...C.medGray); doc.setLineWidth(0.2); doc.line(bx, y + 4, bx, y + bh - 4); }
    doc.setFont("helvetica", "bold"); doc.setFontSize(15); doc.setTextColor(...C.dark);
    doc.text(it.v, bx + PAD, y + 11);
    doc.setFont("helvetica", "normal"); doc.setFontSize(5.5); doc.setTextColor(...C.grayMed);
    doc.text(it.l, bx + PAD, y + 16);
  });
  return y + bh + PAD;
}

