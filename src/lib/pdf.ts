function renderFullTable(doc: jsPDF, result: SearchResult, m: number, y: number, cw: number, ph: number): number {
  // Section header — larger
  doc.setFont("helvetica", "bold"); doc.setFontSize(10); doc.setTextColor(...C.dark);
  doc.text("STORM EVENT DISTRIBUTION ", m, y);
  const tw2 = doc.getTextWidth("STORM EVENT DISTRIBUTION ");
  doc.setFont("helvetica", "normal"); doc.text("(5-YEAR HISTORY)", m + tw2, y);
  y += 6;

  // Thin gold accent line under section header
  doc.setDrawColor(...C.gold); doc.setLineWidth(0.5);
  doc.line(m, y, m + 50, y);
  y += 4;

  // Column positions — wider spacing for larger text
  const cols = {
    date: m + 1, type: m + cw * 0.13, mag: m + cw * 0.21,
    dist: m + cw * 0.31, sev: m + cw * 0.40, status: m + cw * 0.52, radar: m + cw * 0.63,
  };

  // Header row — dark background for contrast
  doc.setFillColor(...C.navy); doc.rect(m, y, cw, 6.5, "F");
  doc.setFont("helvetica", "bold"); doc.setFontSize(7); doc.setTextColor(...C.white);
  doc.text("Date", cols.date, y + 4.5); doc.text("Type", cols.type, y + 4.5);
  doc.text("Magnitude", cols.mag, y + 4.5); doc.text("Distance", cols.dist, y + 4.5);
  doc.text("Severity", cols.sev, y + 4.5); doc.text("Status", cols.status, y + 4.5);
  doc.text("RADAR DATA", cols.radar, y + 4.5);
  y += 8;

  // Leave room for CTA at bottom: stop table earlier
  const tableBottom = ph - 70;

  result.events.forEach((ev, idx) => {
    if (y > tableBottom) { doc.addPage(); y = 15; }

    const radarText = ev.source === "NWS" ? "NWS Official Warning"
      : ev.eventNarrative ? cleanRadar(ev.eventNarrative) : "";

    const isSE = ev.source === "STORM_EVENTS";
    const cleanedNar = isSE && ev.eventNarrative ? cleanRadar(ev.eventNarrative) : "";
    const showDetail = isSE && cleanedNar.length > 10 && !cleanedNar.startsWith("Radar:");
    const rh = showDetail ? 9 : 5.5;

    // Zebra striping
    if (idx % 2 === 0) { doc.setFillColor(...C.zebraGray); doc.rect(m, y - 2.5, cw, rh, "F"); }

    // Subtle row line
    doc.setDrawColor(230, 230, 235); doc.setLineWidth(0.1);
    doc.line(m, y + rh - 2.5, m + cw, y + rh - 2.5);

    // LARGER text — 7pt for main data (was 5.5)
    doc.setFont("helvetica", "normal"); doc.setFontSize(7); doc.setTextColor(...C.dark);
    doc.text(formatEventDate(ev.date), cols.date, y);
    doc.text(ev.type, cols.type, y);

    // Magnitude bold
    doc.setFont("helvetica", "bold");
    doc.text(ev.magnitude ? `${ev.magnitude}${ev.magnitudeUnit === "inches" ? '"' : " mph"}` : "--", cols.mag, y);

    doc.setFont("helvetica", "normal");
    doc.text(`${ev.distanceFromProperty.toFixed(1)} mi`, cols.dist, y);

    // Severity color coded
    doc.setTextColor(...sevColor(ev.severityLabel)); doc.setFont("helvetica", "bold");
    doc.text(dSev(ev.severityLabel), cols.sev, y);

    // Status color coded
    const sc = ev.urgencyStatus === "URGENT" ? C.red : ev.urgencyStatus === "RECENT" ? C.amber : C.gray;
    doc.setTextColor(...sc); doc.text(ev.urgencyStatus, cols.status, y);

    // Radar data — 6.5pt (was 5)
    doc.setFont("helvetica", "normal"); doc.setFontSize(6); doc.setTextColor(...C.grayLight);
    if (radarText) doc.text(radarText, cols.radar, y);

    y += 4.5;

    // Detail row — Storm Events only
    if (showDetail) {
      doc.setFont("helvetica", "italic"); doc.setFontSize(6); doc.setTextColor(...C.grayLight);
      doc.text(cleanedNar.length > 55 ? cleanedNar.substring(0, 55) + "..." : cleanedNar, cols.date + PAD, y);
      y += 4;
    }
  });

  return y + 2;
}

